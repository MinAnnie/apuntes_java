package _01_Conceptos_basicos;

/*
* public -> modificador de acceso
* class -> indica que es una clase
* HolaMundo -> nombre de la clase Java
*  */
public class _01_HolaMundo {
    /*
    * método punto de entrada a nuestro programa, aquí se ejecuta nuestro
    * código.
    *
    * static -> pertenece a la clase (POO)
    * void -> no retorna nada
    * main -> es nuestro método que ejecuta nuestro programa
    *
    *  */
    public static void main(String[] args) {
        /*
        * Imprime en la terminar
        *  */
        System.out.println("Hola mundo uwu");
    }

}
